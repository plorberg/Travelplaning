// Codes Intl may not name; supply German fallbacks.
const FALLBACK_NAMES = { XK: "Kosovo" };

const display = new Intl.DisplayNames(["de"], { type: "region" });
const rows = Object.entries(MAP)
  .map(([code, currency]) => {
    let name = FALLBACK_NAMES[code];
    try {
      const n = display.of(code);
      if (n && n !== code) name = n;
    } catch {}
    return name ? { code, name, currency } : null;
  })
  .filter(Boolean)
  .sort((a, b) => a.name.localeCompare(b.name, "de"));

const lines = rows
  .map((r) => `  { code: "${r.code}", name: ${JSON.stringify(r.name)}, currency: "${r.currency}" },`)
  .join("\n");

const out = `// AUTO-GENERATED by scripts/gen-countries.mjs — do not edit by hand.
// Static country → currency reference (German names). Re-run the script to update.

export type Country = { code: string; name: string; currency: string };

export const COUNTRIES: Country[] = [
${lines}
];

const BY_CODE = new Map(COUNTRIES.map((c) => [c.code, c]));

/** Currency (ISO 4217) for an ISO-3166 alpha-2 country code, or null. */
export function currencyForCountry(code?: string | null): string | null {
  if (!code) return null;
  return BY_CODE.get(code.toUpperCase())?.currency ?? null;
}

/** All distinct currencies, sorted — used to populate the currency dropdown. */
export const CURRENCIES: string[] = Array.from(
  new Set(COUNTRIES.map((c) => c.currency)),
).sort();
`;

writeFileSync("src/lib/countries.ts", out);
console.log(`Wrote src/lib/countries.ts with ${rows.length} countries, ${new Set(rows.map(r=>r.currency)).size} currencies.`);
